class Solution:
    def connect(self, root: 'Optional[Node]') -> 'Optional[Node]':
        if not root:
            return None
        q = collections.deque()
        q.append(root)

        def do(arr):
            for i in range(len(arr) - 1):
                arr[i].next = arr[i + 1]


        while len(q) > 0:
            do(q)
            for _ in range(len(q)):
                node = q.popleft()
                if node.left:
                    q.append(node.left)
                if node.right:
                    q.append(node.right)
        return root